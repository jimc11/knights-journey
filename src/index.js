class KnightTour {
  constructor() {
    this.N = 8;
  }

  //returns 2d int array
  solveKT() {
    var ktGraph = []; // 64 sq w/ 8 possible moves

    // for loops to init
    for (let i = 0; i < 64; i++) {
      ktGraph[i] = [0, 0, 0, 0, 0, 0, 0, 0];
    }

    var nodeId;

    let newPositions;
    let count = 0;

    for (let row = 0; row < 8; row++) {
      for (let col = 0; col < 8; col++) {
        nodeId = this.posToNodeId(row, col);
        newPositions = this.genLegalMoves(row, col);
        //console.log(newPositions)
        count = 0;

        newPositions.forEach((i) => {
          let nid = this.posToNodeId(i[0], i[1]);
          //console.log("nid=" + nid + " cnt = " + i);
          // console.log('i[0]= ' + ktGraph[nodeId][count])
          ktGraph[nodeId][count] = nid; // ...hmmm
          count++;
        });
      }
    }
    //console.log(ktGraph)
    return ktGraph;
  }

  //output is int array
  NodeIdToCoords(id) {
    let floor = Math.floor(id / 8);
    let mod = id % 8;
    return { floor, mod };
  }

  //output is single int
  posToNodeId(row, column) {
    return row * 8 + column;
  }

  //output is 2d int array
  genLegalMoves(x, y) {
    let newMoves = []; // 2d int arr, 8 x 2
    let temp = [0,0]
    for (let i = 0; i < 8; i++){
      newMoves[i] = temp;
    }


    let moveOffsets = [
      [-1, -2],
      [-1, 2],
      [-2, -1],
      [-2, 1],
      [1, -2],
      [1, 2],
      [2, -1],
      [2, 1],
    ]; // 2d int arr, 8 x 2
    for (let i = 0; i < 8; i++) {
      let newX = x + moveOffsets[i][0];
      let newY = y + moveOffsets[i][1];
      if (this.legalCoord(newX) && this.legalCoord(newY))
        newMoves[i] = [newX, newY];
    }
    return newMoves;
  }

  legalCoord(x) {
    if (x >= 0 && x < 8) return true;
    else return false;
  } // end method legalCoord
} // end class KnightTour

class Dijkstra {
  constructor() {}

  algo_dijkstra(graph, src_node) {
    //console.log(''+graph)

    let num_Vertices = 64;
    let path_array=[]; // The output array. dist[i] will hold
    // the shortest distance from src to i

    // spt (shortest path set) contains vertices that have shortest path
    let sptSet = [];
    // Initially all the distances are INFINITE and stpSet[] is set to false
    for (let i = 0; i < num_Vertices; i++) {
      // may be able to set this to 8 instead, for 8 possible connections per chess square
      path_array[i] = 1000;
      sptSet[i] = false;
    }

    // Path between vertex and itself is always 0
    path_array[src_node] = 0;
    // now find shortest path for all vertices
    for (let count = 0; count < num_Vertices - 1; count++) {
      // call minDistance method to find the vertex with min distance
      let u = this.minDistance(path_array, sptSet);
      // the current vertex u is processed
      sptSet[u] = true;
      // process adjacent nodes of the current vertex
      for (let v = 0; v < num_Vertices; v++)
        // if vertex v not in sptset then update it
        if (
          !sptSet[v] &&
          graph[u][v] != 0 &&
          path_array[u] != 1000 &&
          path_array[u] + graph[u][v] < path_array[v]
        )
          path_array[v] = path_array[u] + graph[u][v];
    }
    // console.log(''+path_array)
    // print the path array
    return path_array;
  }

  minDistance(path_array, sptSet) {
    // Initialize min value
    let min = 1000;
    let min_index = -1;
    for (let v = 0; v < 64; v++) {
      // console.log(path_array[v] <= min);
      if (!sptSet[v] && path_array[v] <= min) {
        min = path_array[v];
        min_index = v;
      }
    }
    if (min_index == -1) min_index = 0;
    return min_index;
  }
}

